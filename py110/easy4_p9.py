'''
input: int (id), list (of dicts {id: int, movement: str, quantity: int})
output: list of dicts (only transactions matching the id number)

explicit rules:
- return new list
- include only transactions in first arg

implicit rules:
- 

questions:
- 

data structures:
- list of dicts

algorithm:
1. use id to get list of dicts

'''
def transactions_for(id, transactions):
    return [transaction for transaction in transactions
            if transaction['id'] == id]

transactions = [
    {"id": 101, "movement": 'in',  "quantity":  5},
    {"id": 105, "movement": 'in',  "quantity": 10},
    {"id": 102, "movement": 'out', "quantity": 17},
    {"id": 101, "movement": 'in',  "quantity": 12},
    {"id": 103, "movement": 'out', "quantity": 20},
    {"id": 102, "movement": 'out', "quantity": 15},
    {"id": 105, "movement": 'in',  "quantity": 25},
    {"id": 101, "movement": 'out', "quantity": 18},
    {"id": 102, "movement": 'in',  "quantity": 22},
    {"id": 103, "movement": 'out', "quantity": 15},
]

print(transactions_for(101, transactions) ==
      [
          {"id": 101, "movement": "in",  "quantity":  5},
          {"id": 101, "movement": "in",  "quantity": 12},
          {"id": 101, "movement": "out", "quantity": 18},
      ]) # True
